package algonquin.cst2335.medassist.Main;

/**
 * Enum representing different criteria for sorting medicines.
 *
 * The available sort criteria are:
 * - NAME: Sort medicines by their name.
 * - FREQUENCY: Sort medicines by their frequency.
 * - ADDED: Sort medicines by the order in which they were added.
 */
public enum SortCriteria {
    NAME,
    FREQUENCY,
    ADDED
}
